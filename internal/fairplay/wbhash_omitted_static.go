func wbStaticOmittedRead8(addr uint64) (uint8, bool) {
	page, ok := wbStaticOmittedPages[addr&^uint64(wbStaticPageSize-1)]
	if !ok {
		return 0, false
	}
	return page[addr&uint64(wbStaticPageSize-1)], true
}

func wbStaticOmittedSlice(addr uint64, n int) ([]byte, bool) {
	off := addr & uint64(wbStaticPageSize-1)
	if off+uint64(n) > wbStaticPageSize {
		// Cross-page read: fall back to byte-wise assembly.
		buf := make([]byte, n)
		for i := 0; i < n; i++ {
			b, ok := wbStaticOmittedRead8(addr + uint64(i))
			if !ok {
				return nil, false
			}
			buf[i] = b
		}
		return buf, true
	}
	page, ok := wbStaticOmittedPages[addr&^uint64(wbStaticPageSize-1)]
	if !ok {
		return nil, false
	}
	return page[off : off+uint64(n)], true
}

func wbStaticOmittedRead16(addr uint64) (uint16, bool) {
	b, ok := wbStaticOmittedSlice(addr, 2)
	if !ok {
		return 0, false
	}
	return binary.LittleEndian.Uint16(b), true
}

func wbStaticOmittedRead32(addr uint64) (uint32, bool) {
	b, ok := wbStaticOmittedSlice(addr, 4)
	if !ok {
		return 0, false
	}
	return binary.LittleEndian.Uint32(b), true
}

func wbStaticOmittedRead64(addr uint64) (uint64, bool) {
	b, ok := wbStaticOmittedSlice(addr, 8)
	if !ok {
		return 0, false
	}
	return binary.LittleEndian.Uint64(b), true
}
