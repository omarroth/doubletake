// Each original 256-byte table is an XOR-translated version of a small set of
// byte permutations: base[value XOR inputXOR] XOR outputXOR. Keeping that form
// makes the white-box data explicit without storing hundreds of expanded copies.
type fpsapByteLookup struct {
	table               uint8
	inputXOR, outputXOR byte
}

func (lookup fpsapByteLookup) substitute(value byte) byte {
	return fpsapSubstitutionBases[lookup.table][value^lookup.inputXOR] ^ lookup.outputXOR
}

func (lookup fpsapByteLookup) mix(value byte) byte {
	return fpsapMixBases[lookup.table][value^lookup.inputXOR] ^ lookup.outputXOR
}

type fpsapNetworkTables struct {
	roundSubstitution [9][16]fpsapByteLookup
	mixColumns        [4][4]fpsapByteLookup
	finalSubstitution [16]fpsapByteLookup
}

