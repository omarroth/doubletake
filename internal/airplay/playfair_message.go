// FairPlay message encryption uses the standard inverse AES round operations,
// but its four modes have custom middle round keys rather than AES key
// schedules. crypto/aes therefore cannot represent this block transform.
func decryptMessage(message, plaintext []byte) {
	mode := message[12]
	for step := 0; step < 8; step++ {
		block := step
		if mode == 3 {
			// Mode 3 historically traverses the CBC chain backwards. This is
			// equivalent for separate buffers and also permits in-place use.
			block = 7 - step
		}
		start := 16 + block*16
		var state [16]byte
		copy(state[:], message[start:start+16])
		decryptPlayfairMessageBlock(&state, mode)

		chain := playfairMessageIV[mode][:]
		if block > 0 {
			chain = message[start-16 : start]
		}
		for i := range state {
			plaintext[block*16+i] = state[i] ^ chain[i]
		}
	}
}

func decryptPlayfairMessageBlock(state *[16]byte, mode byte) {
	xorPlayfairRoundKey(state, &playfairMessageRoundKey10)
	for round := 9; round > 0; round-- {
		inverseAESShiftRows(state)
		for i := range state {
			state[i] = inverseAESSBox[state[i]]
		}
		xorPlayfairRoundKey(state, &playfairMessageMiddleKeys[mode][round-1])
		inverseAESMixColumns(state)
	}
	inverseAESShiftRows(state)
	for i := range state {
		state[i] = inverseAESSBox[state[i]]
	}
	xorPlayfairRoundKey(state, &playfairMessageRoundKey0)
}

func xorPlayfairRoundKey(state, key *[16]byte) {
	for i := range state {
		state[i] ^= key[i]
	}
}

func inverseAESShiftRows(state *[16]byte) {
	previous := *state
	for row := 0; row < 4; row++ {
		for column := 0; column < 4; column++ {
			state[4*column+row] = previous[4*((column-row+4)&3)+row]
		}
	}
}

func inverseAESMixColumns(state *[16]byte) {
	for column := 0; column < 4; column++ {
		offset := column * 4
		a, b, c, d := state[offset], state[offset+1], state[offset+2], state[offset+3]
		state[offset] = aesGFMultiply(a, 14) ^ aesGFMultiply(b, 11) ^ aesGFMultiply(c, 13) ^ aesGFMultiply(d, 9)
		state[offset+1] = aesGFMultiply(a, 9) ^ aesGFMultiply(b, 14) ^ aesGFMultiply(c, 11) ^ aesGFMultiply(d, 13)
		state[offset+2] = aesGFMultiply(a, 13) ^ aesGFMultiply(b, 9) ^ aesGFMultiply(c, 14) ^ aesGFMultiply(d, 11)
		state[offset+3] = aesGFMultiply(a, 11) ^ aesGFMultiply(b, 13) ^ aesGFMultiply(c, 9) ^ aesGFMultiply(d, 14)
	}
}

func aesGFMultiply(a, b byte) byte {
	var product byte
	for b != 0 {
		if b&1 != 0 {
			product ^= a
		}
		high := a & 0x80
		a <<= 1
		if high != 0 {
			a ^= 0x1b
		}
		b >>= 1
	}
	return product
}

